# Prog04: Create a program that ask user to input 2 numbers. Print the product 
# of the two numbers.

# Pseudocode
# - input 2 numbers
# - multiply the two numbers
# - print product
    
multiplicand = float(input("Enter the multiplicand: "))
multiplier = float(input("Enter the multiplier: "))

product = multiplicand * multiplier

print(f"The product of {multiplicand} and {multiplier} is {product}")