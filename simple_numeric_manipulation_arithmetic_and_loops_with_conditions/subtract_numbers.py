# Prog03: Create a program that ask user to input 2 numbers. Print the 
# difference of the two numbers.

# Pseudocode
# - input 2 numbers
# - subtract the minuend to the subtrahend
# - print result

minuend = float(input("Enter the minuend: "))
subtrahend = float(input("Enter the subtrahend: "))

difference = minuend - subtrahend

print(f"{minuend} minus {subtrahend} is {difference}.")